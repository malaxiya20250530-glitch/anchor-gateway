def collect_all_facts():
    """收集所有领域的生成事实"""
    all_facts = []
    generators = [
        ("国家与首都", gen_country_facts),
        ("化学元素", gen_element_facts),
        ("历史人物", gen_figure_facts),
        ("科学常识", None),  # 直接是列表 (含物理/天文/生物/化学/计算机/发明)
        ("地理常识", None),
        ("常见误解", None),
    ]
    # 注意：科技发明已合并到科学常识列表中

    for name, gen_func in generators:
        if gen_func:
            facts = gen_func()
            print(f"  {name}: {len(facts)} 条事实")
            all_facts.extend(facts)
        else:
            if name == "科学常识":
                facts = SCIENCE_FACTS
            elif name == "地理常识":
                facts = GEOGRAPHY_FACTS
            elif name == "常见误解":
                facts = MISCONCEPTIONS
            else:
                facts = []
            print(f"  {name}: {len(facts)} 条事实")
            all_facts.extend(facts)

    return all_facts


def main():
    write_mode = "--write" in sys.argv

    print("=" * 60)
    print("  真实世界知识生成器")
    print("  目标: 扩充 hallucination_detector 知识库")
    print(f"  模式: {'写入' if write_mode else '预览（不写入）'}")
    print("=" * 60)

    # 收集所有事实
    print("\n📋 收集事实...")
    all_facts = collect_all_facts()
    print(f"\n  总计: {len(all_facts)} 条待处理事实")

    if not write_mode:
        # 预览模式：显示统计和样例
        sources = {}
        for _, source, _, _ in all_facts:
            sources[source] = sources.get(source, 0) + 1
        print("\n📊 来源分布:")
        for src, cnt in sorted(sources.items(), key=lambda x: -x[1]):
            print(f"  {src}: {cnt} 条")

        print("\n📝 样例 (前15条):")
        for fact, source, conf, entity in all_facts[:15]:
            print(f"  [{source}] {fact} (置信度:{conf}, 实体:{entity})")
        print(f"\n  使用 --write 参数实际写入数据库")
        return

    # 写入模式
    print("\n💾 连接数据库...")
    if not DB_PATH.exists():
        print(f"  ❌ 数据库不存在: {DB_PATH}")
        sys.exit(1)

    conn = sqlite3.connect(str(DB_PATH))
    conn.execute("PRAGMA journal_mode=WAL")
    conn.execute("PRAGMA synchronous=OFF")

    # 先查询已有哈希集合（采样去重——全量加载太慢）
    print("  📥 采样已有哈希用于去重...")
    t0 = time.time()
    existing = set()
    # 只采样最近的事实和一个随机子集
    for row in conn.execute("SELECT hash FROM facts_0 WHERE rowid % 100 = 0 LIMIT 100000"):
        existing.add(row[0])
    print(f"  已采样 {len(existing):,} 条哈希 ({time.time()-t0:.1f}s)")

    # 插入
    inserted = 0
    skipped = 0
    batch = []
    batch_size = 100

    t0 = time.time()
    for fact, source, confidence, entity_id in all_facts:
        h = stable_hash(fact)

        # 采样去重检查
        if h in existing:
            skipped += 1
            continue

        batch.append((h, fact, source, confidence, entity_id))
        existing.add(h)  # 批次内去重

        if len(batch) >= batch_size:
            conn.executemany(
                """INSERT OR IGNORE INTO facts_0 
                   (hash, fact, source, confidence, created_at, hash_version, active, entity_id)
                   VALUES (?, ?, ?, ?, datetime('now'), 'sha256_v1', 1, ?)""",
                batch
            )
            inserted += len(batch)
            batch = []

            if inserted % 500 == 0:
                elapsed = time.time() - t0
                print(f"  [{inserted}/{len(all_facts)}] 已插入 | {inserted/elapsed:.0f} 条/s")

    # 剩余批次
    if batch:
        conn.executemany(
            """INSERT OR IGNORE INTO facts_0 
               (hash, fact, source, confidence, created_at, hash_version, active, entity_id)
               VALUES (?, ?, ?, ?, datetime('now'), 'sha256_v1', 1, ?)""",
            batch
        )
        inserted += len(batch)

    conn.commit()

    # 更新计数器
    total = conn.execute("SELECT COUNT(*) FROM facts_0").fetchone()[0]
    conn.execute(
        "INSERT OR REPLACE INTO fact_counters(name, value) VALUES('active_count', ?), ('total', ?)",
        (total, total)
    )
    conn.commit()
    conn.close()

    elapsed = time.time() - t0
    print(f"\n{'=' * 60}")
    print(f"  ✅ 完成!")
    print(f"  📥 新增: {inserted:,} 条")
    print(f"  ⏭️  跳过: {skipped:,} 条")
    print(f"  📦 总事实数: {total:,} 条")
    print(f"  ⏱️  耗时: {elapsed:.1f}s")
    print(f"{'=' * 60}")


if __name__ == "__main__":
    main()
